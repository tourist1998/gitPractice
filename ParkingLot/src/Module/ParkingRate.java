package Module;

public class ParkingRate {
    private Integer FirstHourRate;
    private Integer SecondHourRate;
    private Integer NormalRates;

    public Integer getFirstHourRate() {
        return FirstHourRate;
    }

    public void setFirstHourRate(Integer firstHourRate) {
        FirstHourRate = firstHourRate;
    }

    public Integer getSecondHourRate() {
        return SecondHourRate;
    }

    public void setSecondHourRate(Integer secondHourRate) {
        SecondHourRate = secondHourRate;
    }

    public Integer getNormalRates() {
        return NormalRates;
    }

    public void setNormalRates(Integer normalRates) {
        NormalRates = normalRates;
    }
}
